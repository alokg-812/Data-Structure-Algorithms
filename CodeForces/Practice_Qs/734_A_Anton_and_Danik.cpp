#include<bits/stdc++.h>
using namespace std;
int main() {
    int n;
    cin>>n;
    string A[n];
    int count_A = 0;
    int count_D = 0;
    for(int i = 0 ; i < n ; i++){
        if(A[i] == 'A'){
            count_A++;
        }
        if(A[i] = 'D'){
            count_D++;
        }
    }
}